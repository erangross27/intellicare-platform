      // Collect this record's pending edits that belong to this section (localEdits key = "<fieldPart>-<idx>").
      const suffix = `-${idx}`;
      const toCommit = Object.keys(localEdits).filter(k => pendingEdits[k] && k.endsWith(suffix) && fields.includes(k.slice(0, -suffix.length)));
      // Persist each staged field to the DB now, replicating the payload the original per-save would have sent.
      for (const editKey of toCommit) {
        const fieldPart = editKey.slice(0, -suffix.length); // e.g. "crabCriteria.calcium", "recommendations", "results"
        const value = localEdits[editKey];
        if (Array.isArray(value)) {
          // array field (recommendations): one PUT per element, with arrayIndex
          for (let i = 0; i < value.length; i++) {
            const resp = await secureApiClient.put(`/api/edit/myeloma_specific_data/${id}/edit`, { field: fieldPart, value: value[i], arrayIndex: i });
            if (resp && resp.success === false) throw new Error(resp.error || 'save failed');
          }
        } else if (value && typeof value === 'object') {
          // dynamic-key object (results): one PUT per leaf key, field = "results.<key>"
          for (const k of Object.keys(value)) {
            const resp = await secureApiClient.put(`/api/edit/myeloma_specific_data/${id}/edit`, { field: `${fieldPart}.${k}`, value: value[k] });
            if (resp && resp.success === false) throw new Error(resp.error || 'save failed');
          }
        } else {
          // plain string field (fieldPart may itself contain a dot, e.g. crabCriteria.calcium — NO arrayIndex)
          const resp = await secureApiClient.put(`/api/edit/myeloma_specific_data/${id}/edit`, { field: fieldPart, value });
          if (resp && resp.success === false) throw new Error(resp.error || 'save failed');
        }
      }
      // Flag the record/section approved (audit trail)
      await secureApiClient.put(`/api/edit/myeloma_specific_data/${id}/approve`, { sectionId: sid, approved: true });

      // Clear pending → committed edits now flow into pdfData/PDF
      setPendingEdits(prev => { const n = { ...prev }; toCommit.forEach(k => delete n[k]); return n; });
      // Drop this section's committed fields from the localStorage draft store
