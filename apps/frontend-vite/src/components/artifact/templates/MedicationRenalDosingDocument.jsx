/* parseLabel: detect "Label: value" patterns (skip subordinate-clause openers; strip inline list "N." markers) */
const CLAUSE_OPENER = /^(if|when|while|unless|although|though|because|since|after|before|once|given|whether|should|as|until|provided|assuming|in case)\b/i;
const parseLabel = (text) => {
  if (!text || typeof text !== 'string') return { isLabeled: false, label: '', value: text || '' };
  const m = text.match(/^([A-Za-z][A-Za-z0-9\s/&(),.#'"-]{1,60}?):\s+([\s\S]*)/);
  if (m && !CLAUSE_OPENER.test(m[1].trim())) return { isLabeled: true, label: m[1].trim(), value: m[2].trim().replace(/^\d+\.\s+/, '') };
  return { isLabeled: false, label: '', value: text };
};

/* splitByComma: parenthesis-aware comma split (thousands-guarded: comma must be followed by whitespace) */
const splitByComma = (text) => {
  if (!text || typeof text !== 'string') return [text || ''];
  const result = []; let current = ''; let depth = 0;
  for (let i = 0; i < text.length; i++) {
    const ch = text[i];
    if (ch === '(') { depth++; current += ch; }
    else if (ch === ')') { depth = Math.max(0, depth - 1); current += ch; }
    else if (ch === ',' && depth === 0 && /\s/.test(text[i + 1] || '')) { const t = current.trim(); if (t) result.push(t); current = ''; }
    else { current += ch; }
  }
  const t = current.trim(); if (t) result.push(t);
  return result.length > 0 ? result : [text];
};

/* peelLabels: recursively strip nested "Outer: Inner: value" labels into stacked subtitles + a bare value
   (handles the inline-numbered-list first item "Heading: 1. Sub: text" without a side-by-side "Sub: value" row) */
const peelLabels = (text) => {
  const labels = []; let value = String(text || ''); let guard = 0;
  let p = parseLabel(value);
  while (p.isLabeled && splitByComma(p.value).length < 2 && guard++ < 6) {
    labels.push(p.label); value = p.value; p = parseLabel(value);
  }
  return { labels, value: value.replace(/[;.]+$/, '').trim() };
};

/* humanizeKey: camelCase / snake_case object key → Title Case label */
const humanizeKey = (key) => {
  if (key === null || key === undefined || key === '') return '';
  const s = String(key).replace(/_/g, ' ').replace(/([a-z0-9])([A-Z])/g, '$1 $2').replace(/([A-Z]+)([A-Z][a-z])/g, '$1 $2');
  return s.charAt(0).toUpperCase() + s.slice(1);
};

/* objectCopyLines: nested object → box-free copy lines (label line, value line — never side-by-side "Label: value") */
const objectCopyLines = (obj) => {
  const lines = [];
  const walk = (o, indent) => {
    Object.entries(o).forEach(([k, v]) => {
      if (v === null || v === undefined || v === '') return;
      const label = humanizeKey(k);
      if (Array.isArray(v)) {
        const items = v.filter(x => x !== null && x !== undefined && x !== '');
        if (items.length === 0) return;
        lines.push(`${indent}${label}`);
        items.forEach((it, i) => lines.push(`${indent}  ${i + 1}. ${typeof it === 'object' ? JSON.stringify(it) : it}`));
      } else if (v && typeof v === 'object') {
        if (Object.keys(v).length === 0) return;
        lines.push(`${indent}${label}`);
        walk(v, indent + '  ');
      } else {
        lines.push(`${indent}${label}`);
        lines.push(`${indent}${typeof v === 'boolean' ? (v ? 'Yes' : 'No') : String(v)}`);
      }
    });
  };
  walk(obj, '');
  return lines;
};

