const getWorkStatusSeverity = (status) => {
  if (!status) return null;
  const statusLower = String(status).toLowerCase();
  if (statusLower.includes('full duty') || statusLower === 'full') return { label: 'Full Duty', color: '#22c55e', description: 'Cleared for all work activities', percentage: 100 };
  if (statusLower.includes('modified') || statusLower.includes('light duty')) return { label: 'Modified Duty', color: '#f59e0b', description: 'Work with restrictions', percentage: 60 };
  if (statusLower.includes('off work') || statusLower.includes('disability') || statusLower.includes('unable')) return { label: 'Off Work', color: '#ef4444', description: 'Unable to work', percentage: 20 };
  return { label: status, color: '#3b82f6', description: 'Current work status', percentage: 50 };
};

const getApportionmentSeverity = (percentage) => {
  if (percentage === null || percentage === undefined) return null;
  const pct = Number(percentage);
  if (isNaN(pct)) return null;
  if (pct >= 90) return { label: `${pct}% Work-Related`, color: '#ef4444', description: 'Primarily occupational causation', percentage: pct };
  if (pct >= 50) return { label: `${pct}% Work-Related`, color: '#f59e0b', description: 'Significant occupational contribution', percentage: pct };
  if (pct >= 25) return { label: `${pct}% Work-Related`, color: '#3b82f6', description: 'Partial occupational contribution', percentage: pct };
  return { label: `${pct}% Work-Related`, color: '#22c55e', description: 'Minor occupational contribution', percentage: pct };
};

const getImpairmentSeverity = (rating) => {
  if (rating === null || rating === undefined) return null;
  const pct = Number(rating);
  if (isNaN(pct)) return null;
  if (pct === 0) return { label: 'No Impairment', color: '#22c55e', description: 'No permanent disability', percentage: 5 };
  if (pct <= 10) return { label: `${pct}% Impairment`, color: '#3b82f6', description: 'Mild permanent impairment', percentage: Math.max(pct, 10) };
  if (pct <= 25) return { label: `${pct}% Impairment`, color: '#f59e0b', description: 'Moderate permanent impairment', percentage: pct };
  return { label: `${pct}% Impairment`, color: '#ef4444', description: 'Significant permanent impairment', percentage: pct };
};

