/* Flatten an object to key-value items for display */
const flattenObject = (obj) => {
  if (!obj || typeof obj !== 'object' || Array.isArray(obj)) return [];
  const items = [];
  Object.entries(obj).forEach(([key, value]) => {
    if (value === null || value === undefined || value === '') return;
    const label = key.replace(/([A-Z])/g, ' $1').replace(/^./, str => str.toUpperCase());
    if (typeof value === 'boolean') {
      items.push({ label, value: value ? 'Yes' : 'No', key });
    } else if (Array.isArray(value)) {
      items.push({ label, value: value.filter(Boolean).join(', '), key });
    } else if (typeof value === 'object') {
      Object.entries(value).forEach(([subKey, subValue]) => {
        if (subValue !== null && subValue !== undefined && subValue !== '') {
          const subLabel = `${label} - ${subKey.replace(/([A-Z])/g, ' $1').replace(/^./, s => s.toUpperCase())}`;
          items.push({ label: subLabel, value: String(subValue), key: `${key}.${subKey}` });
        }
      });
    } else {
      items.push({ label, value: String(value), key });
    }
  });
  return items;
};

