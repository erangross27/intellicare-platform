/* humanizeKey: Total_Bilirubin -> "Total Bilirubin" */
const humanizeKey = (key) => String(key).replace(/_/g, ' ').replace(/([a-z])([A-Z])/g, '$1 $2').replace(/\s+/g, ' ').trim().replace(/\b\w/g, c => c.toUpperCase());

/* flattenResults: recursively flatten dynamic-key object into { keyPath, label, value } rows.
   keyPath is the dot-path under results (for dot-path saves); value is typed-leaf string. */
const flattenResults = (obj, prefix = '', labelPrefix = '') => {
  if (!obj || typeof obj !== 'object' || Array.isArray(obj)) return [];
  const rows = [];
  Object.keys(obj).forEach((k) => {
    const v = obj[k];
    const keyPath = prefix ? `${prefix}.${k}` : k;
    const label = labelPrefix ? `${labelPrefix} - ${humanizeKey(k)}` : humanizeKey(k);
    if (v === null || v === undefined || v === '') return;
    if (typeof v === 'object' && !Array.isArray(v) && !v.$date) {
      rows.push(...flattenResults(v, keyPath, label));
    } else if (Array.isArray(v)) {
      const joined = v.filter(x => x !== null && x !== undefined && x !== '').map(x => (typeof x === 'object' ? JSON.stringify(x) : String(x))).join(', ');
      if (joined) rows.push({ keyPath, label, value: joined, isLeafEditable: false });
    } else if (typeof v === 'boolean') {
      rows.push({ keyPath, label, value: v ? 'Yes' : 'No', isLeafEditable: false });
    } else {
      rows.push({ keyPath, label, value: String(v), isLeafEditable: true });
    }
  });
  return rows;
};

