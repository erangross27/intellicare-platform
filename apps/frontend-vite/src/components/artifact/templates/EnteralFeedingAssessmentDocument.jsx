    const fullText = items.join(', ');
    setSaveError(null);
    stageDraft(id, fn, idx, sid, fullText);
    const commaKey = `${fn}-${idx}-c${ciIdx}`;
    const marks = { [commaKey]: 'edited' };
    for (let ei = 1; ei < subParts.length; ei++) marks[`${fn}-${idx}-c${ciIdx + ei}`] = 'added';
