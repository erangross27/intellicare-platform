    const currentVal = String(getFieldValue(record, fn, idx) || '');
    const isSemicolon = SEMICOLON_FIELDS.includes(fn);
    const sentences = isSemicolon ? splitBySemicolon(currentVal) : splitBySentence(currentVal);
