      for (const editKey of toCommit) {
        const suffix = `-${idx}`;
        const field = editKey.slice(0, -suffix.length); // plain field name (no dot suffix in this template)
        await secureApiClient.put(`/api/edit/exercise_recommendations/${id}/edit`, { field, value: localEdits[editKey] });
      }
      // Flag the record/section approved (audit trail)
      await secureApiClient.put(`/api/edit/exercise_recommendations/${id}/approve`, { sectionId: sid, approved: true });

      // Clear pending → committed edits now flow into pdfData/PDF
      setPendingEdits(prev => { const n = { ...prev }; toCommit.forEach(k => delete n[k]); return n; });
      // Drop this section's drafts from localStorage (now committed)
      const store = readDrafts();
