/* parseLabel: paren-aware "Label: value" detection — the first ": " at paren depth 0 splits, so
   labels with time-range parens like "Early labor phase (04:30-07:00)" keep their inner colons. */
const parseLabel = (text) => {
  if (!text || typeof text !== 'string') return null;
  let depth = 0;
  for (let i = 0; i < text.length; i++) {
    const ch = text[i];
    if (ch === '(') depth++;
    else if (ch === ')') depth = Math.max(0, depth - 1);
    else if (ch === ':' && depth === 0) {
      if (!/\s/.test(text[i + 1] || '')) return null;
      const label = text.slice(0, i).trim();
      const content = text.slice(i + 1).trim();
      if (/^[A-Za-z]/.test(label) && label.length >= 3 && label.length <= 60 && content) return { label, content };
      return null;
    }
  }
  return null;
};

/* splitByComma: paren-aware; keep Oxford ", and/or X"; skip no-space commas ("$18,000") and date commas ("January 8, 2026"). */
const splitByComma = (text) => {
  if (!text || typeof text !== 'string') return [];
  const parts = []; let cur = ''; let depth = 0;
  for (let i = 0; i < text.length; i++) {
    const ch = text[i];
    if (ch === '(') depth++;
    else if (ch === ')') depth = Math.max(0, depth - 1);
    if (ch === ',' && depth === 0) {
      const rest = text.slice(i + 1);
      if (!/^\s/.test(rest)) { cur += ch; continue; }
      if (/^\s+(?:and|or)\b/i.test(rest)) { cur += ch; continue; }
      if (/\d\s*$/.test(cur) && /^\s*\d{4}\b/.test(rest)) { cur += ch; continue; }
      const t = cur.trim(); if (t) parts.push(t); cur = '';
    } else cur += ch;
  }
  if (cur.trim()) parts.push(cur.trim());
  return parts.filter(Boolean);
};

