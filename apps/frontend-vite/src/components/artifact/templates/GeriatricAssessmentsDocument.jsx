        const tail = lastDot === -1 ? '' : fieldPart.slice(lastDot + 1);
        const isArrayElem = lastDot !== -1 && /^\d+$/.test(tail);
        const baseField = isArrayElem ? fieldPart.slice(0, lastDot) : fieldPart;
        if (!fields.includes(baseField)) continue; // not part of this section
        const payload = { field: baseField, value: recDrafts[fieldPart] };
