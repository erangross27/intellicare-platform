/* parseLabel: detect "Label: value" patterns (skip subordinate-clause openers) */
const CLAUSE_OPENER = /^(if|when|while|unless|although|though|because|since|after|before|once|given|whether|should|as|until|provided|assuming|in case)\b/i;
const parseLabel = (text) => {
  if (!text || typeof text !== 'string') return { isLabeled: false, label: '', value: text || '' };
  const m = text.match(/^([A-Za-z][A-Za-z0-9\s/&(),.#'"-]{1,60}?):\s+([\s\S]*)/);
  if (m && !CLAUSE_OPENER.test(m[1].trim())) return { isLabeled: true, label: m[1].trim(), value: m[2].trim().replace(/^\d+\.\s+/, '') };
  return { isLabeled: false, label: '', value: text };
};

/* splitByComma: parenthesis-aware comma split */
const splitByComma = (text) => {
  if (!text || typeof text !== 'string') return [text || ''];
  const result = []; let current = ''; let depth = 0;
  for (let i = 0; i < text.length; i++) {
    const ch = text[i];
    if (ch === '(') { depth++; current += ch; }
    else if (ch === ')') { depth = Math.max(0, depth - 1); current += ch; }
    else if (ch === ',' && depth === 0 && /\s/.test(text[i + 1] || '')) { const t = current.trim(); if (t) result.push(t); current = ''; }
    else { current += ch; }
  }
  const t = current.trim(); if (t) result.push(t);
  return result.length > 0 ? result : [text];
};

const formatDate = (dateValue) => {
  if (!dateValue) return '';
  try { const d = new Date(dateValue.$date || dateValue); return d.toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' }); } catch { return String(dateValue); }
};

const toInputDate = (dateValue) => {
  if (!dateValue) return '';
  try { const d = new Date(dateValue.$date || dateValue); return d.toISOString().split('T')[0]; } catch { return ''; }
};

const formatOperativeTime = (timeObj) => {
  if (!timeObj) return '';
  if (typeof timeObj === 'string') return timeObj;
  if (typeof timeObj !== 'object') return String(timeObj);
  const parts = [];
  if (timeObj.totalDuration) parts.push(`Total: ${timeObj.totalDuration}`);
  if (timeObj.duration) parts.push(`Duration: ${timeObj.duration}`);
  if (timeObj.cpbTime) parts.push(`CPB Time: ${timeObj.cpbTime}`);
  if (timeObj.aorticCrossClampTime) parts.push(`Aortic Cross-Clamp: ${timeObj.aorticCrossClampTime}`);
  if (timeObj.startTime) parts.push(`Start: ${timeObj.startTime}`);
  if (timeObj.endTime) parts.push(`End: ${timeObj.endTime}`);
  if (timeObj.totalMinutes) parts.push(`Duration: ${timeObj.totalMinutes} minutes`);
  return parts.join(' | ') || '';
};

