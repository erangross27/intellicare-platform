  // committed values now flow into pdfData/PDF. This is the ONLY path that writes to the database.
  const handleApproveSection = useCallback(async (record, sid, idx) => {
    const id = safeId(record); if (!id) return;
    setSaving(true); setSaveError(null);
    const fields = SECTION_FIELDS[sid] || [];
    try {
      // Collect this record's pending edits whose base field belongs to this section.
      const suffix = `-${idx}`;
      const toCommit = Object.keys(pendingEdits).filter(k => {
        if (!pendingEdits[k] || !k.endsWith(suffix)) return false;
        const fieldPart = k.slice(0, -suffix.length); // "field" or "field.arrayIndex"
        const lastDot = fieldPart.lastIndexOf('.');
        const baseField = (lastDot !== -1 && /^\d+$/.test(fieldPart.slice(lastDot + 1))) ? fieldPart.slice(0, lastDot) : fieldPart;
        return fields.includes(baseField);
      });
      for (const editKey of toCommit) {
        const fieldPart = editKey.slice(0, -suffix.length);
        const lastDot = fieldPart.lastIndexOf('.');
        const isArrayItem = lastDot !== -1 && /^\d+$/.test(fieldPart.slice(lastDot + 1));
        if (isArrayItem) {
          const baseField = fieldPart.slice(0, lastDot);
          const arrIdx = parseInt(fieldPart.slice(lastDot + 1), 10);
          const arr = localEdits[`${baseField}-${idx}`];
          const value = Array.isArray(arr) ? arr[arrIdx] : undefined;
          await secureApiClient.put(`/api/edit/fall_prevention_education/${id}/edit`, { field: baseField, value, arrayIndex: arrIdx });
        } else {
          await secureApiClient.put(`/api/edit/fall_prevention_education/${id}/edit`, { field: fieldPart, value: localEdits[`${fieldPart}-${idx}`] });
        }
      }
      // Flag the section approved (audit trail)
      await secureApiClient.put(`/api/edit/fall_prevention_education/${id}/approve`, { sectionId: sid, approved: true });

      // Clear pending → committed edits now flow into pdfData/PDF
      setPendingEdits(prev => { const n = { ...prev }; toCommit.forEach(k => delete n[k]); return n; });
      // Drop this record's drafts for these fields from localStorage (now committed)
      const store = readDrafts();
      if (store[id]) {
        Object.keys(store[id]).forEach(fieldPart => {
          const lastDot = fieldPart.lastIndexOf('.');
          const baseField = (lastDot !== -1 && /^\d+$/.test(fieldPart.slice(lastDot + 1))) ? fieldPart.slice(0, lastDot) : fieldPart;
          if (fields.includes(baseField)) delete store[id][fieldPart];
        });
        if (Object.keys(store[id]).length === 0) delete store[id];
        writeDrafts(store);
      }

      setApprovedSections(prev => ({ ...prev, [`${sid}-${idx}`]: true }));
      setEditedFields(prev => { const n = { ...prev }; Object.keys(n).forEach(k => { fields.forEach(f => { if (k.startsWith(`${f}-${idx}`) || (k.startsWith(`${f}.`) && k.endsWith(`-${idx}`))) delete n[k]; }); }); return n; });
      setEditedSentences(prev => { const n = { ...prev }; Object.keys(n).forEach(k => { fields.forEach(f => { if (k.startsWith(`${f}-${idx}`)) delete n[k]; }); }); return n; });
    } catch (err) {
      console.error('[FallPreventionEducation] Approve error:', err);
      setSaveError('Save failed. Please try again.');
    } finally { setSaving(false); }
  }, [safeId, pendingEdits, localEdits]);

