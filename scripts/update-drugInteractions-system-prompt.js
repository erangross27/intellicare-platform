#!/usr/bin/env node
/**
 * Update medications system prompt to use new drugInteractions object structure
 *
 * OLD STRUCTURE (array):
 * "drugInteractions": [
 *   { "medication": "...", "severity": "...", "description": "...", "source": "..." }
 * ]
 *
 * NEW STRUCTURE (object with counts):
 * "drugInteractions": {
 *   "totalInteractions": 2,
 *   "contraindicated": 0,
 *   "major": 1,
 *   "moderate": 1,
 *   "minor": 0,
 *   "interactions": [
 *     { "interactsWith": "...", "severity": "...", "description": "...", "source": "..." }
 *   ]
 * }
 */

const fs = require('fs');
const path = require('path');

const promptsPath = path.join(__dirname, '../apps/backend-api/services/collectionSystemPrompts.json');
const prompts = require(promptsPath);

if (!prompts.medications) {
  console.error('❌ No medications system prompt found!');
  process.exit(1);
}

let medPrompt = prompts.medications;

// 1. Update the COMPLETE EXAMPLE to use new object structure
const oldExamplePattern = /"drugInteractions": \[\s*\{[^}]*"medication": "Potassium supplements"[^}]*\}[^\]]*\]/s;
const newExample = `"drugInteractions": {
    "totalInteractions": 2,
    "contraindicated": 0,
    "major": 1,
    "moderate": 1,
    "minor": 0,
    "interactions": [
      {
        "interactsWith": "Potassium supplements",
        "severity": "MAJOR",
        "description": "Increased risk of hyperkalemia when combined with ACE inhibitors",
        "source": "FDA Drug Interaction Database"
      },
      {
        "interactsWith": "NSAIDs",
        "severity": "MODERATE",
        "description": "May reduce antihypertensive effect and increase risk of renal impairment",
        "source": "FDA Drug Interaction Database"
      }
    ]
  }`;

if (oldExamplePattern.test(medPrompt)) {
  medPrompt = medPrompt.replace(oldExamplePattern, newExample);
  console.log('✅ Updated COMPLETE EXAMPLE with new drugInteractions structure');
} else {
  console.log('⚠️  Could not find old example pattern to replace');
}

// 2. Update the field description in CRITICAL EXTRACTION RULES
const oldFieldDescPattern = /• drugInteractions:.*?(?=\n\*\*|$)/s;
const newFieldDesc = `• drugInteractions: CRITICAL OBJECT STRUCTURE - Contains interaction analysis with severity counts AND detailed interaction list. Structure: { totalInteractions: number, contraindicated: number, major: number, moderate: number, minor: number, interactions: [{ interactsWith: string (other drug name), severity: string (CONTRAINDICATED/MAJOR/MODERATE/MINOR), description: string, source: string }] }. Generated by medication safety checker during save operation - NOT extracted from document. This field is auto-populated by backend medicationSafetyChecker service.`;

if (oldFieldDescPattern.test(medPrompt)) {
  medPrompt = medPrompt.replace(oldFieldDescPattern, newFieldDesc);
  console.log('✅ Updated field description in CRITICAL EXTRACTION RULES');
} else {
  console.log('⚠️  Could not find field description to replace');
}

// 3. Save updated prompt
prompts.medications = medPrompt;
fs.writeFileSync(promptsPath, JSON.stringify(prompts, null, 2));

console.log('\n✅ Successfully updated medications system prompt!');
console.log('\nChanges made:');
console.log('  1. Updated COMPLETE EXAMPLE to show new object structure with counts');
console.log('  2. Changed field name from "medication" to "interactsWith"');
console.log('  3. Updated field description to document object structure');
console.log('  4. Noted that field is auto-populated by medicationSafetyChecker');
